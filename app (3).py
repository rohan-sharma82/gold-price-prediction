import streamlit as st
import pandas as pd
import pickle

# Load the trained model
# Ensure 'random_forest_model.pkl' is in the same directory as this script
with open('random_forest_model.pkl', 'rb') as file:
    model = pickle.load(file)

st.title('Gold Price Prediction App')
st.write('Enter the values for the features to predict the GLD price.')

# Input fields for features
spx = st.slider('SPX Index', min_value=600.0, max_value=3000.0, value=1500.0)
uso = st.slider('USO Price', min_value=5.0, max_value=120.0, value=30.0)
slv = st.slider('SLV Price', min_value=5.0, max_value=50.0, value=20.0)
eur_usd = st.slider('EUR/USD Exchange Rate', min_value=1.0, max_value=1.6, value=1.2)

# Create a DataFrame for prediction
# Ensure the column names and order match the training data
input_data = pd.DataFrame([[spx, uso, slv, eur_usd]],
                          columns=['SPX', 'USO', 'SLV', 'EUR/USD'])

if st.button('Predict Gold Price (GLD)'):
    prediction = model.predict(input_data)[0]
    st.success(f'The predicted GLD Price is: ${prediction:.2f}')